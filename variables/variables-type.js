// 3 tipos de variables
// var
var myVariable = "Soy un string";
console.log(myVariable);
myVariable = 15;
console.log(myVariable);
myVariable = true;
console.log(myVariable);
myVariable = [1, 2, 3];
console.log(myVariable);
// let
let myOtherVariable = "Soy otro string";
console.log(myOtherVariable);
myOtherVariable = 15;
console.log(myOtherVariable);
// const
const myConstVariable = "soy const String";
console.log(myConstVariable);
// sin tipo
soyVariableSinTipo = "Soy sin tipo";
console.log(soyVariableSinTipo);
soyVariableSinTipo = [1,1,1,];
console.log(soyVariableSinTipo);